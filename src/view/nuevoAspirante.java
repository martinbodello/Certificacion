    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        Competidor nuevo = new Competidor();
        
        nuevo.setApellido(txtApellido.getText());
        nuevo.setNombre(txtNombres.getText());
        nuevo.setDni(Integer.parseInt(txtDni.getText()));
        nuevo.setDireccion(txtDireccion.getText());
        nuevo.setApellido(txtApellido.getText());
        nuevo.setEscuela(e);
           
        if (femenino.isSelected())         nuevo.setSexo("F");
        if (masculino.isSelected())        nuevo.setSexo("M");
        
        
        DateFormat format = new SimpleDateFormat("yyyy-MM-dd", Locale.ENGLISH);
        Date date = null;
        try {
            date = format.parse(txtFechaNac.getText());
        } catch (ParseException ex) {
            Logger.getLogger(nuevoAspirante.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        nuevo.setFechaNac(date);
        
        Session session = NewHibernateUtil.getSessionFactory().openSession();
        session.beginTransaction();
 
        session.save(nuevo); //<|--- Aqui guardamos el objeto en la base de datos.
 
        session.getTransaction().commit();
        session.close();
        
        this.setVisible(false);
        inscripcion i = new inscripcion();
        i.setVisible(true);    
    }//GEN-LAST:event_btnAceptarActionPerformed


